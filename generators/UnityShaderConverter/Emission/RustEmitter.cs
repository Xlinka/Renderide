    private static void EmitMaterialUniformBlock(StringBuilder sb, IReadOnlyList<ShaderPropertyRecord> properties)
    {
        bool anyScalar = properties.Any(static p => !IsTexturePropertyKind(p.Kind));
        if (!anyScalar)
            return;

        (IReadOnlyList<Std140UniformField> fields, _) = MaterialUniformStd140Layout.Build(properties);
        sb.AppendLine("/// Uniform buffer layout matching the prepended WGSL `MaterialUniform` (std140 with explicit padding).");
        sb.AppendLine("#[repr(C, align(16))]");
        sb.AppendLine("#[allow(dead_code)]");
        sb.AppendLine("pub struct MaterialUniform {");
        foreach (Std140UniformField f in fields)
        {
            sb.Append("    pub ").Append(f.RustFieldName).Append(": ").Append(f.RustType).AppendLine(",");
        }

        sb.AppendLine("}");
        sb.AppendLine();
    }

    private static void EmitMaterialBindGroupRust(StringBuilder sb, IReadOnlyList<ShaderPropertyRecord> properties)
    {
        bool anyScalar = properties.Any(static p => !IsTexturePropertyKind(p.Kind));
        sb.AppendLine("/// Bind group 2: `MaterialUniform` at binding 0, then each texture + sampler pair.");
        sb.AppendLine("fn push_material_bind_group_layout_entries(entries: &mut Vec<wgpu::BindGroupLayoutEntry>) {");
        if (anyScalar)
        {
            sb.AppendLine("    entries.push(wgpu::BindGroupLayoutEntry {");
            sb.AppendLine("        binding: 0,");
            sb.AppendLine("        visibility: wgpu::ShaderStages::VERTEX_FRAGMENT,");
            sb.AppendLine("        ty: wgpu::BindingType::Buffer {");
            sb.AppendLine("            ty: wgpu::BufferBindingType::Uniform,");
            sb.AppendLine("            has_dynamic_offset: false,");
            sb.AppendLine("            min_binding_size: None,");
            sb.AppendLine("        },");
            sb.AppendLine("        count: None,");
            sb.AppendLine("    });");
        }

        uint b = 1;
        foreach (ShaderPropertyRecord prop in properties)
        {
            if (!IsTexturePropertyKind(prop.Kind))
                continue;
            sb.AppendLine("    entries.push(wgpu::BindGroupLayoutEntry {");
            sb.Append("        binding: ").Append(b.ToString(CultureInfo.InvariantCulture)).AppendLine(",");
            sb.AppendLine("        visibility: wgpu::ShaderStages::FRAGMENT,");
            sb.AppendLine("        ty: wgpu::BindingType::Texture {");
            sb.AppendLine("            sample_type: wgpu::TextureSampleType::Float { filterable: true },");
            sb.Append("            view_dimension: ");
            sb.AppendLine(TextureViewDimensionRust(prop.Kind));
            sb.AppendLine("            multisampled: false,");
            sb.AppendLine("        },");
            sb.AppendLine("        count: None,");
            sb.AppendLine("    });");
            b++;
            sb.AppendLine("    entries.push(wgpu::BindGroupLayoutEntry {");
            sb.Append("        binding: ").Append(b.ToString(CultureInfo.InvariantCulture)).AppendLine(",");
            sb.AppendLine("        visibility: wgpu::ShaderStages::FRAGMENT,");
            sb.AppendLine("        ty: wgpu::BindingType::Sampler(wgpu::SamplerBindingType::Filtering),");
            sb.AppendLine("        count: None,");
            sb.AppendLine("    });");
            b++;
        }

        if (!anyScalar && !properties.Any(static p => IsTexturePropertyKind(p.Kind)))
            sb.AppendLine("    let _ = entries;");

        sb.AppendLine("}");
        sb.AppendLine();
        sb.AppendLine("/// Returns `(layout, scene_uniform_size_bytes)` for WGSL `@group(1)` clustered-forward scene data.");
        sb.AppendLine("pub fn create_renderide_scene_bind_group_layout(device: &wgpu::Device, label: &str) -> (wgpu::BindGroupLayout, u64) {");
        sb.AppendLine("    let mut entries = Vec::new();");
        sb.AppendLine("    push_renderide_scene_bind_group_layout_entries(&mut entries);");
        sb.AppendLine("    let scene_uniform_size = std::mem::size_of::<crate::gpu::pipeline::SceneUniforms>() as u64;");
        sb.AppendLine("    let layout = device.create_bind_group_layout(&wgpu::BindGroupLayoutDescriptor {");
        sb.AppendLine("        label: Some(label),");
        sb.AppendLine("        entries: &entries,");
        sb.AppendLine("    });");
        sb.AppendLine("    (layout, scene_uniform_size)");
        sb.AppendLine("}");
        sb.AppendLine();
        sb.AppendLine("pub fn create_material_bind_group_layout(device: &wgpu::Device, label: &str) -> wgpu::BindGroupLayout {");
        sb.AppendLine("    let mut entries = Vec::new();");
        sb.AppendLine("    push_material_bind_group_layout_entries(&mut entries);");
        sb.AppendLine("    device.create_bind_group_layout(&wgpu::BindGroupLayoutDescriptor {");
        sb.AppendLine("        label: Some(label),");
        sb.AppendLine("        entries: &entries,");
        sb.AppendLine("    })");
        sb.AppendLine("}");
        sb.AppendLine();
        sb.AppendLine("/// `texture_views_and_samplers` must contain one `(view, sampler)` per texture property in Properties order.");
        sb.AppendLine("pub fn create_material_bind_group(");
        sb.AppendLine("    device: &wgpu::Device,");
        sb.AppendLine("    layout: &wgpu::BindGroupLayout,");
        sb.AppendLine("    label: &str,");
        sb.AppendLine("    material_uniform_buffer: &wgpu::Buffer,");
        sb.AppendLine("    texture_views_and_samplers: &[(&wgpu::TextureView, &wgpu::Sampler)],");
        sb.AppendLine(") -> wgpu::BindGroup {");
        sb.AppendLine("    let mut entries: Vec<wgpu::BindGroupEntry> = Vec::new();");
        if (!anyScalar)
            sb.AppendLine("    let _ = material_uniform_buffer;");
        if (anyScalar)
        {
            sb.AppendLine("    entries.push(wgpu::BindGroupEntry {");
            sb.AppendLine("        binding: 0,");
            sb.AppendLine("        resource: wgpu::BindingResource::Buffer(wgpu::BufferBinding {");
            sb.AppendLine("            buffer: material_uniform_buffer,");
            sb.AppendLine("            offset: 0,");
            sb.AppendLine("            size: None,");
            sb.AppendLine("        }),");
            sb.AppendLine("    });");
        }

        sb.AppendLine("    let mut b: u32 = 1;");
        sb.AppendLine("    for (view, sampler) in texture_views_and_samplers {");
        sb.AppendLine("        entries.push(wgpu::BindGroupEntry {");
        sb.AppendLine("            binding: b,");
        sb.AppendLine("            resource: wgpu::BindingResource::TextureView(view),");
        sb.AppendLine("        });");
        sb.AppendLine("        b += 1;");
        sb.AppendLine("        entries.push(wgpu::BindGroupEntry {");
        sb.AppendLine("            binding: b,");
        sb.AppendLine("            resource: wgpu::BindingResource::Sampler(sampler),");
        sb.AppendLine("        });");
        sb.AppendLine("        b += 1;");
        sb.AppendLine("    }");
        sb.AppendLine("    device.create_bind_group(&wgpu::BindGroupDescriptor { label: Some(label), layout, entries: &entries })");
        sb.AppendLine("}");
        sb.AppendLine();
    }

    private static string EscapeRustString(string s)
    {
        return s.Replace("\\", "\\\\").Replace("\"", "\\\"");
    }

    private static string RustFieldName(string uniformName)
    {
        string n = uniformName.TrimStart('_');
        if (n.Length == 0)
            n = "unnamed";
        string lowered = $"{char.ToLowerInvariant(n[0])}{n.AsSpan(1)}";
        return RustEscapeIdent(lowered);
    }

    private static string RustEscapeIdent(string s)
    {
        if (s.Length > 0 && char.IsDigit(s[0]))
            return "u_" + s;
        return s;
    }

    private static string TextureViewDimensionRust(ShaderPropertyKind k) =>
        k switch
        {
            ShaderPropertyKind.TextureCube => "wgpu::TextureViewDimension::Cube,",
            ShaderPropertyKind.Texture2DArray => "wgpu::TextureViewDimension::D2Array,",
            ShaderPropertyKind.TextureCubeArray => "wgpu::TextureViewDimension::CubeArray,",
            ShaderPropertyKind.Texture3D or ShaderPropertyKind.Texture3DArray => "wgpu::TextureViewDimension::D3,",
            ShaderPropertyKind.Texture2D or ShaderPropertyKind.TextureAny => "wgpu::TextureViewDimension::D2,",
            _ => "wgpu::TextureViewDimension::D2,",
        };

    private static string RustPropertyType(ShaderPropertyRecord prop) =>
        prop.Kind switch
        {
            ShaderPropertyKind.Float or ShaderPropertyKind.Range => "f32",
            ShaderPropertyKind.Integer or ShaderPropertyKind.Int => "i32",
            ShaderPropertyKind.Color or ShaderPropertyKind.Vector => "Vec4",
            ShaderPropertyKind.Texture2D or ShaderPropertyKind.Texture3D or ShaderPropertyKind.TextureCube
                or ShaderPropertyKind.TextureAny or ShaderPropertyKind.Texture2DArray
                or ShaderPropertyKind.Texture3DArray or ShaderPropertyKind.TextureCubeArray => "TextureHandle",
            _ => "()",
        };

    private static string DefaultRustValue(ShaderPropertyRecord prop) =>
        prop.Kind switch
        {
            ShaderPropertyKind.Float or ShaderPropertyKind.Range =>
                prop.DefaultSummary.Length > 0 && float.TryParse(prop.DefaultSummary, CultureInfo.InvariantCulture, out float f)
                    ? F32Lit(f)
                    : "0.0",
            ShaderPropertyKind.Integer or ShaderPropertyKind.Int =>
                prop.DefaultSummary.Length > 0 && int.TryParse(prop.DefaultSummary, CultureInfo.InvariantCulture, out int i)
                    ? i.ToString(CultureInfo.InvariantCulture)
                    : "0",
            ShaderPropertyKind.Color or ShaderPropertyKind.Vector =>
                ParseVec4Default(prop.DefaultSummary),
            ShaderPropertyKind.Texture2D or ShaderPropertyKind.Texture3D or ShaderPropertyKind.TextureCube
                or ShaderPropertyKind.TextureAny or ShaderPropertyKind.Texture2DArray
                or ShaderPropertyKind.Texture3DArray or ShaderPropertyKind.TextureCubeArray => "0",
            _ => "()",
        };

    private static string ParseVec4Default(string summary)
    {
        ReadOnlySpan<char> s = summary.AsSpan().Trim();
        if (s.Length < 5 || s[0] != '(')
            return "Vec4::ONE";
        s = s[1..^1];
        var parts = new List<float>();
        foreach (string part in s.ToString().Split(','))
        {
            if (float.TryParse(part.Trim(), CultureInfo.InvariantCulture, out float v))
                parts.Add(v);
        }

        if (parts.Count >= 4)
            return $"Vec4::new({F32Lit(parts[0])}, {F32Lit(parts[1])}, {F32Lit(parts[2])}, {F32Lit(parts[3])})";
        if (parts.Count == 3)
            return $"Vec4::new({F32Lit(parts[0])}, {F32Lit(parts[1])}, {F32Lit(parts[2])}, 1.0)";
        return "Vec4::ONE";
    }

    private static string F32Lit(float x)
    {
        string s = x.ToString("G9", CultureInfo.InvariantCulture);
        if (!s.Contains('.') && !s.Contains('E') && !s.Contains('e'))
            s += ".0";
        return s;
    }

    /// <summary>
    /// Unity ShaderLab sometimes stores <c>Range</c> defaults as integers; <see cref="DefaultRustValue"/> may
    /// emit <c>4</c> while the Rust field is <c>f32</c>. Parses and rewrites with <see cref="F32Lit"/>.
    /// </summary>
    private static string CoerceRustF32DefaultLiteral(string expr)
    {
        if (float.TryParse(expr, NumberStyles.Float, CultureInfo.InvariantCulture, out float f))
            return F32Lit(f);
        return expr;
    }

    private static string EscapeComment(string s) => s.Replace("*/", "* /");
