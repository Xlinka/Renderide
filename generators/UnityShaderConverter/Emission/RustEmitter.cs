    }

    /// <summary>Full <c>material.rs</c>: <c>VariantKey</c>, <c>Material</c>, <c>include_str!</c> per pass, wgpu helpers.</summary>
    public static string EmitShaderMaterialRs(
        ShaderFileDocument document,
        string sourcePathForComment,
        int passCount,
        IReadOnlyList<SpecializationAxis> specializationAxes,
        IReadOnlyList<PassVertexLayout> vertexLayoutsPerPass,
        uint sceneBindGroupIndex = 1,
        uint materialBindGroupIndex = 2)
    {
        var sb = new StringBuilder();
        sb.AppendLine("//! @generated by UnityShaderConverter — do not edit.");
        sb.Append("//! Unity shader: `").Append(document.ShaderName).AppendLine("`");
        sb.Append("//! Source: `").Append(sourcePathForComment.Replace("\\", "/")).AppendLine("`");
        sb.AppendLine("//! One WGSL file per pass (named from ShaderLab `Name` or `passN`).");
        sb.AppendLine("//! WGSL `override` / `@id` constants use `wgpu::PipelineCompilationOptions` on vertex and fragment stages (wgpu 28).");
        sb.AppendLine("//! Texture properties on `Material` use `TextureHandle` (default `0`); host defines invalid/unbound semantics.");
        sb.AppendLine();
        sb.AppendLine("#![allow(");
        sb.AppendLine("    non_snake_case,");
        sb.AppendLine("    unused_imports,");
        sb.AppendLine("    unused_variables,");
        sb.AppendLine("    clippy::derivable_impls,");
        sb.AppendLine("    clippy::excessive_precision,");
        sb.AppendLine("    clippy::ptr_arg,");
        sb.AppendLine(")]");
        sb.AppendLine();
        sb.AppendLine("use glam::Vec4;");
        if (document.Properties.Any(static p => MaterialUniformStd140Layout.IsTexturePropertyKind(p.Kind)))
            sb.AppendLine("use crate::scene::types::TextureHandle;");
        sb.AppendLine();
        EmitVariantKeyStruct(sb, specializationAxes);
        sb.AppendLine("/// WGSL clustered scene uniforms, lights, cluster buffers (matches builtin PBR).");
        sb.Append("pub const RENDERIDE_SCENE_BIND_GROUP: u32 = ").Append(sceneBindGroupIndex.ToString(CultureInfo.InvariantCulture)).AppendLine(";");
        sb.AppendLine("/// WGSL `MaterialUniform` and texture properties group index.");
        sb.Append("pub const MATERIAL_BIND_GROUP: u32 = ").Append(materialBindGroupIndex.ToString(CultureInfo.InvariantCulture)).AppendLine(";");
        sb.AppendLine();
        EmitRenderideSceneBindGroupLayout(sb);
        for (int pi = 0; pi < passCount; pi++)
        {
            string file = pi < document.Passes.Count
                ? WgslPassFileName(document.Passes[pi], pi)
                : WgslPassFileName(pi);
            sb.Append("/// Merged vertex + fragment WGSL for pass ").Append(pi).AppendLine(".");
            sb.Append("pub const PASS").Append(pi).Append("_WGSL: &str = include_str!(\"");
            sb.Append(file).AppendLine("\");");
            sb.AppendLine();
        }

        sb.AppendLine("/// Default material values mapped from the Unity Properties block.");
        sb.AppendLine("#[derive(Clone, Debug, PartialEq)]");
        sb.AppendLine("pub struct Material {");
        foreach (ShaderPropertyRecord prop in document.Properties)
        {
            sb.Append("    pub ").Append(RustFieldName(prop.Name)).Append(": ")
                .Append(RustPropertyType(prop)).Append(", // ")
                .AppendLine(EscapeComment(prop.DisplayLabel));
        }

        sb.AppendLine("}");
        sb.AppendLine();
        sb.AppendLine("impl Default for Material {");
        sb.AppendLine("    fn default() -> Self {");
        sb.AppendLine("        Self {");
        foreach (ShaderPropertyRecord prop in document.Properties)
        {
            string rustType = RustPropertyType(prop);
            string valueExpr = DefaultRustValue(prop);
            if (rustType == "f32")
                valueExpr = CoerceRustF32DefaultLiteral(valueExpr);
            sb.Append("            ").Append(RustFieldName(prop.Name)).Append(": ")
                .Append(valueExpr).AppendLine(",");
        }

        sb.AppendLine("        }");
        sb.AppendLine("    }");
        sb.AppendLine("}");
        sb.AppendLine();
        EmitMaterialDynamicRenderStateHelpers(sb, document);
        EmitPipelineHelpers(sb, document, specializationAxes, vertexLayoutsPerPass);
        if (document.Passes.Count > 0)
        {
            for (int i = 0; i < document.Passes.Count; i++)
            {
                sb.Append("/// Vertex shader entry name produced by Slang for pass ").Append(i).AppendLine(".");
                sb.Append("pub const VERTEX_ENTRY_PASS").Append(i).Append(": &str = \"");
                sb.Append(EscapeRustString(document.Passes[i].VertexEntry!)).AppendLine("\";");
                sb.Append("pub const FRAGMENT_ENTRY_PASS").Append(i).Append(": &str = \"");
                sb.Append(EscapeRustString(document.Passes[i].FragmentEntry!)).AppendLine("\";");
                if (document.Passes[i].PragmaShaderTarget is float tgt)
                {
                    int tenths = (int)Math.Round(tgt * 10.0f, MidpointRounding.AwayFromZero);
                    sb.Append("/// First `#pragma target` in pass ").Append(i).AppendLine(" (x10 for integer const).");
                    sb.Append("pub const PRAGMA_SHADER_TARGET_PASS").Append(i).Append(": u32 = ")
                        .Append(tenths.ToString(CultureInfo.InvariantCulture)).AppendLine(";");
                }

                sb.AppendLine();
            }

            sb.AppendLine("/// Pass names from ShaderLab (for future pipeline / file naming).");
            for (int i = 0; i < document.Passes.Count; i++)
            {
                string? pn = document.Passes[i].PassName;
                string label = string.IsNullOrEmpty(pn) ? $"pass_{i}" : pn;
                sb.Append("pub const PASS_").Append(i).Append("_NAME: &str = \"");
                sb.Append(EscapeRustString(label)).AppendLine("\";");
            }
        }

        return sb.ToString();
    }

    private static void EmitVariantKeyStruct(StringBuilder sb, IReadOnlyList<SpecializationAxis> axes)
    {
        if (axes.Count == 0)
        {
            sb.AppendLine("/// No `multi_compile` specialization axes were extracted; pipeline constants are unused.");
            sb.AppendLine("#[derive(Clone, Copy, Debug, Default, PartialEq, Eq, Hash)]");
            sb.AppendLine("pub struct VariantKey;");
            sb.AppendLine();
            return;
        }

        sb.AppendLine("/// Maps to WGSL `override` / `wgpu::PipelineCompilationOptions::constants` (decimal id keys per wgpu docs).");
        sb.AppendLine("#[derive(Clone, Copy, Debug, PartialEq, Eq, Hash)]");
        sb.AppendLine("pub struct VariantKey {");
        foreach (SpecializationAxis ax in axes)
        {
            sb.Append("    /// ShaderLab keyword `").Append(EscapeComment(ax.Keyword)).AppendLine("`");
            sb.Append("    pub ").Append(ax.RustFieldName).AppendLine(": bool,");
        }

        sb.AppendLine("}");
        sb.AppendLine();
        sb.AppendLine("impl Default for VariantKey {");
        sb.AppendLine("    fn default() -> Self {");
        sb.AppendLine("        Self {");
        foreach (SpecializationAxis ax in axes)
            sb.Append("            ").Append(ax.RustFieldName).AppendLine(": false,");

        sb.AppendLine("        }");
        sb.AppendLine("    }");
        sb.AppendLine("}");
        sb.AppendLine();
    }

    private static void EmitPipelineHelpers(
        StringBuilder sb,
        ShaderFileDocument document,
        IReadOnlyList<SpecializationAxis> axes,
        IReadOnlyList<PassVertexLayout> vertexLayoutsPerPass)
    {
        int passCount = document.Passes.Count;
        sb.AppendLine("/// WGSL source for use with `Device::create_shader_module`.");
        for (int pi = 0; pi < passCount; pi++)
        {
            string fnStem = ShaderNaming.WgslPassStem(document.Passes[pi].PassName, pi);
            sb.Append("pub fn shader_source_").Append(fnStem).AppendLine("() -> wgpu::ShaderSource<'static> {");
            sb.Append("    wgpu::ShaderSource::Wgsl(PASS").Append(pi).AppendLine("_WGSL.into())");
            sb.AppendLine("}");
            sb.AppendLine();
        }

        EmitPipelineHelpersDynamicAware(sb, document, axes, vertexLayoutsPerPass);
    }

    private static void EmitPassVertexBufferLayout(StringBuilder sb, PassVertexLayout layout, int passIndex)
    {
        if (!layout.HasAttributes)
        {
            sb.Append("/// No interleaved vertex attributes were extracted for pass ").Append(passIndex);
            sb.AppendLine("; supply `vertex_layout_override` or fix WGSL.");
            sb.Append("pub static VERTEX_BUFFER_LAYOUTS_PASS").Append(passIndex);
            sb.AppendLine(": &[wgpu::VertexBufferLayout] = &[];");
            sb.AppendLine();
            return;
        }

        sb.Append("static VERTEX_ATTRIBUTES_PASS").Append(passIndex).AppendLine(": &[wgpu::VertexAttribute] = &[");
        for (int i = 0; i < layout.Attributes.Count; i++)
        {
            VertexLayoutAttribute a = layout.Attributes[i];
            uint off = layout.ByteOffsets[i];
            sb.Append("    wgpu::VertexAttribute { format: ").Append(a.RustVertexFormatPath);
            sb.Append(", offset: ").Append(off.ToString(CultureInfo.InvariantCulture));
            sb.Append(", shader_location: ").Append(a.ShaderLocation.ToString(CultureInfo.InvariantCulture));
            sb.AppendLine(" },");
        }

        sb.AppendLine("];");
        sb.Append("static VERTEX_BUFFER_LAYOUT_PASS").Append(passIndex).AppendLine(": [wgpu::VertexBufferLayout; 1] = [wgpu::VertexBufferLayout {");
        sb.Append("    array_stride: ").Append(layout.ArrayStride.ToString(CultureInfo.InvariantCulture)).AppendLine(",");
        sb.AppendLine("    step_mode: wgpu::VertexStepMode::Vertex,");
        sb.Append("    attributes: VERTEX_ATTRIBUTES_PASS").Append(passIndex).AppendLine(",");
        sb.AppendLine("}];");
        sb.Append("pub static VERTEX_BUFFER_LAYOUTS_PASS").Append(passIndex);
        sb.Append(": &[wgpu::VertexBufferLayout] = &VERTEX_BUFFER_LAYOUT_PASS").Append(passIndex).AppendLine(";");
        sb.AppendLine();
    }

    private static void EmitSpecializationPipelineOptions(StringBuilder sb, IReadOnlyList<SpecializationAxis> axes, int passCount)
    {
        if (axes.Count > 0)
        {
            sb.AppendLine("/// Maps `VariantKey` fields to pipeline constant ids (decimal strings per WebGPU).");
            sb.AppendLine("pub fn specialization_constants_f64(variant: &VariantKey) -> std::vec::Vec<(&'static str, f64)> {");
            sb.AppendLine("    let mut v = std::vec::Vec::new();");
            foreach (SpecializationAxis ax in axes)
            {
                sb.Append("    v.push((\"");
                sb.Append(ax.ConstantId.ToString(CultureInfo.InvariantCulture));
                sb.Append("\", if variant.").Append(ax.RustFieldName).AppendLine(" { 1.0 } else { 0.0 }));");
            }

            sb.AppendLine("    v");
            sb.AppendLine("}");
            sb.AppendLine();
            sb.AppendLine("static SPECIALIZATION_KEYS: &[&str] = &[");
            foreach (SpecializationAxis ax in axes)
            {
                sb.Append("    \"");
                sb.Append(ax.ConstantId.ToString(CultureInfo.InvariantCulture));
                sb.AppendLine("\",");
            }

            sb.AppendLine("];");
            sb.AppendLine();
            sb.AppendLine("fn pipeline_compilation_options_vertex_inner(variant: &VariantKey) -> wgpu::PipelineCompilationOptions<'static> {");
            sb.Append("    let vals: [f64; ").Append(axes.Count.ToString(CultureInfo.InvariantCulture)).AppendLine("] = [");
            foreach (SpecializationAxis ax in axes)
            {
                sb.Append("        if variant.").Append(ax.RustFieldName).AppendLine(" { 1.0 } else { 0.0 },");
            }

            sb.AppendLine("    ];");
            sb.AppendLine("    let tuples: std::vec::Vec<(&'static str, f64)> = SPECIALIZATION_KEYS");
            sb.AppendLine("        .iter()");
            sb.AppendLine("        .copied()");
            sb.AppendLine("        .zip(vals)");
            sb.AppendLine("        .collect();");
            sb.AppendLine("    let boxed = tuples.into_boxed_slice();");
            sb.AppendLine("    let leaked = Box::leak(boxed);");
            sb.AppendLine("    wgpu::PipelineCompilationOptions {");
            sb.AppendLine("        constants: leaked,");
            sb.AppendLine("        ..Default::default()");
            sb.AppendLine("    }");
            sb.AppendLine("}");
            sb.AppendLine();
        }
        else
        {
            sb.AppendLine("fn pipeline_compilation_options_vertex_inner(_variant: &VariantKey) -> wgpu::PipelineCompilationOptions<'static> {");
            sb.AppendLine("    wgpu::PipelineCompilationOptions::default()");
            sb.AppendLine("}");
            sb.AppendLine();
        }

        sb.AppendLine("/// Builds `PipelineCompilationOptions` for vertex/fragment stages (WGSL `override` / `@id`).");
        sb.AppendLine("/// Note: leaks a tiny slice per call; cache by `VariantKey` in hot paths.");
        sb.AppendLine("/// Per-pass wrappers delegate to the same implementation (specialization is per-shader).");
        for (int pi = 0; pi < passCount; pi++)
        {
            sb.Append("pub fn pipeline_compilation_options_pass").Append(pi).AppendLine("_vertex(variant: &VariantKey) -> wgpu::PipelineCompilationOptions<'static> {");
            sb.AppendLine("    pipeline_compilation_options_vertex_inner(variant)");
            sb.AppendLine("}");
            sb.AppendLine();
            sb.Append("pub fn pipeline_compilation_options_pass").Append(pi).AppendLine("_fragment(variant: &VariantKey) -> wgpu::PipelineCompilationOptions<'static> {");
            sb.AppendLine("    pipeline_compilation_options_vertex_inner(variant)");
            sb.AppendLine("}");
            sb.AppendLine();
        }
    }

